/** Exponential backoff with small jitter: ~100ms, ~250ms, ~600ms, capped at 2s. */
async function sleepBackoff(attempt: number): Promise<void> {
  const base = Math.min(2000, 100 * Math.pow(2.2, attempt));
  const jitter = Math.random() * 50;
  await new Promise((r) => setTimeout(r, base + jitter));
}

/** Polyfill for runtimes without `AbortSignal.timeout()`. */
function createTimeoutSignal(ms: number): AbortSignal {
  const controller = new AbortController();
  setTimeout(() => controller.abort(), ms);
  return controller.signal;
}

